# Store Anton's age
anton = 21

# Calculate Beth's age (6 years older than Anton)
beth = anton + 6

# Calculate Chen's age (20 years older than Beth)
chen = beth + 20

# Calculate Drew's age (sum of Chen and Anton's ages)
drew = chen + anton

# Ethan's age (same as Chen)
ethan = chen

# Print everyone's ages
print(f"Anton is {anton} years old.")
