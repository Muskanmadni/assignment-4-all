
def main():
    num1 = float(input("Enter the first number: "))
    num2 = float(input("Enter the second number: "))
    result = num1 / num2
    remainder = num1 % num2
    print(f"The result of dividing {num1} by {num2} is {result:.2f} and the remainder is {remainder:.2f}.")

if __name__ == "__main__":
    main()


